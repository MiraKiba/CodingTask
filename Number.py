num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))
num3 = int(input("Enter the third integer: "))

# Calculate the sum of all three numbers
total_sum = num1 + num2 + num3

# Calculate the difference between the first and second numbers
difference = num1 - num2

# Calculate the product of the third and first numbers
product = num3 * num1

# Calculate the sum of all three numbers divided by the third number
division_result = total_sum / num3

print("Sum of all numbers:", total_sum)
print("First number minus second number:", difference)
print("Third number multiplied by the first number:", product)
print("Sum of all numbers divided by third number:", division_result)